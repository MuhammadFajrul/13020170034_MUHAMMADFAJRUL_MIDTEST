package com.example.tugasproject4;

public class CastandCrew {
    String nama;
    int photocast;

    public CastandCrew(String nama, int photo) {
        this.nama = nama;
        this.photocast = photo;
    }

    public String getNama() {
        return nama;
    }

    public int getPhoto() {
        return photocast;
    }
}
